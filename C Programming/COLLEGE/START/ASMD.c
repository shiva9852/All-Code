// WAP TO DISPLAY ADD,SUBTRACT,MULTIPLY AND DIVISION
#include<stdio.h>
int main(){
    int x,y;
    printf("ENTER THE NUMBERS = ");
    scanf("%d%d",&x,&y);
    printf("ADD= %d\n",x+y);
    printf("SUBTRACT= %d\n",x-y);
    printf("MULTIPLY= %d\n",x*y);
    printf("DIVISION= %d\n",x/y);
    return 0;
}